export function getDefaultLang(): Lang {
  const navLang = navigator.language.slice(0, 2);
  return LANGS.includes(navLang as Lang) ? (navLang as Lang) : 'ja';
}
export function t(key: string, lang: Lang = getDefaultLang(), fallback: string = ''): string {
  return I18N[lang][key] || fallback || key;
} 
